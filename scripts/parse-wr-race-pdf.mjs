function buildPayload() {
    const parsedRows = ROWS.map((parts) => ({
        distance: parts[0],
        samples: BOATS.map((_, i) => ({
            speed: parts[1 + i * 2],
            stroke: parts[2 + i * 2],
        })),
    }));

    const boats = BOATS.map((id, i) => {
        const firstSpeed = parsedRows[0].samples[i].speed;
        const points = [
            { distance: 0, speed: firstSpeed },
            ...parsedRows.map((row) => ({
                distance: row.distance,
                speed: row.samples[i].speed,
            })),
        ];
        return {
            id,
            label: id,
            rank: RANKS[i],
            color: COLORS[i],
            points,
        };
    });

    return {
        source: 'WCp1_2026_1_ROWMSCULL1------------HEAT000100--_C77X2406.pdf',
        event: "Men's Single Sculls",
        round: 'H1',
        race: '5',
        venue: 'Seville, ESP · 29 May 2026',
        courseLength: 2000,
        markers: [0, 500, 1000, 1500, 2000],
        yAxis: { min: 3.5, max: 6, unit: 'm/s' },
        boats,
    };
}

writeFileSync(OUT, JSON.stringify(buildPayload(), null, 2));
